def combine_split_menu(da):
    print("\nChoose an option:")
    print("1. Combine Arrays")
    print("2. Split Array")
    choice = DataAnalytics.get_int_input("Enter your choice: ")

    if choice == 1:
        size = da.array.size
        elements = read_elements(size, f"Enter the elements of another array to combine ({size} elements separated by space): ")
        other = np.array(elements).reshape(da.array.shape)
        print("\nOriginal Array:")
        print(da)
        print("\nSecond Array:")
        print(other)
        print("\nCombined Array (Vertical Stack):")
        print(da.combine(other, axis=0))

    elif choice == 2:
        n = DataAnalytics.get_int_input("Enter the number of splits: ")
        parts = da.split(n)
        print("\nSplit Arrays:")
        for i, p in enumerate(parts, 1):
            print(f"Part {i}:")
            print(p)
    else:
        print("Invalid choice.")


def search_sort_filter_menu(da):
    print("\nChoose an option:")
    print("1. Search a value")
    print("2. Sort the array")
    print("3. Filter values")
    choice = DataAnalytics.get_int_input("Enter your choice: ")

    print("\nOriginal Array:")
    print(da)

    if choice == 1:
        value = float(input("Enter the value to search for: "))
        result = da.search(value)
        print("\nValue found at indices:" if result.size else "\nValue not found.")
        if result.size:
            print(result)

    elif choice == 2:
        order = input("Sort ascending or descending? (a/d): ").strip().lower()
        sorted_arr = da.sort(ascending=(order != "d"))
        print("\nSorted Array:")
        print(sorted_arr)
        print("(Sorting applied row-wise.)")

    elif choice == 3:
        op = input("Filter condition (>, <, >=, <=, ==): ").strip()
        val = float(input("Value to compare against: "))
        conditions = {
            ">": lambda a: a > val,
            "<": lambda a: a < val,
            ">=": lambda a: a >= val,
            "<=": lambda a: a <= val,
            "==": lambda a: a == val,
        }
        if op in conditions:
            print("\nFiltered Values:")
            print(da.filter_values(conditions[op]))
        else:
            print("Invalid condition.")
    else:
        print("Invalid choice.")


