def read_elements(count, prompt):
    """Read `count` numeric elements typed on one line separated by spaces."""
    while True:
        raw = input(prompt).split()
        if len(raw) != count:
            print(f"Please enter exactly {count} elements.")
            continue
        try:
            return [float(x) if "." in x else int(x) for x in raw]
        except ValueError:
            print("Invalid input, numbers only.")


def create_array_menu():
    print("\nSelect the type of array to create:")
    print("1. 1D Array")
    print("2. 2D Array")
    print("3. 3D Array")
    choice = DataAnalytics.get_int_input("Enter your choice: ")

    if choice == 1:
        n = DataAnalytics.get_int_input("Enter the number of elements: ")
        elements = read_elements(n, f"Enter {n} elements for the array separated by space: ")
        da = DataAnalytics.create_from_input(1, (n,), elements)

    elif choice == 2:
        rows = DataAnalytics.get_int_input("Enter the number of rows: ")
        cols = DataAnalytics.get_int_input("Enter the number of columns: ")
        n = rows * cols
        elements = read_elements(n, f"Enter {n} elements for the array separated by space: ")
        da = DataAnalytics.create_from_input(2, (rows, cols), elements)

    elif choice == 3:
        d1 = DataAnalytics.get_int_input("Enter size of dimension 1: ")
        d2 = DataAnalytics.get_int_input("Enter size of dimension 2: ")
        d3 = DataAnalytics.get_int_input("Enter size of dimension 3: ")
        n = d1 * d2 * d3
        elements = read_elements(n, f"Enter {n} elements for the array separated by space: ")
        da = DataAnalytics.create_from_input(3, (d1, d2, d3), elements)

    else:
        print("Invalid choice.")
        return None

    print("\nArray created successfully:")
    print(da)
    return da


def indexing_slicing_menu(da):
    while True:
        print("\nChoose an operation:")
        print("1. Indexing")
        print("2. Slicing")
        print("3. Go Back")
        choice = DataAnalytics.get_int_input("Enter your choice: ")

        if choice == 1:
            idx_str = input("Enter index (comma separated, e.g. 0,1): ")
            idx = tuple(int(i) for i in idx_str.split(","))
            print("Element:", da.index_element(idx))

        elif choice == 2:
            row_range = input("Enter the row range (start:end): ")
            r_start, r_end = (int(x) for x in row_range.split(":"))
            if da.array.ndim > 1:
                col_range = input("Enter the column range (start:end): ")
                c_start, c_end = (int(x) for x in col_range.split(":"))
                result = da.slice_array(slice(r_start, r_end), slice(c_start, c_end))
            else:
                result = da.slice_array(slice(r_start, r_end))
            print("\nSliced Array:")
            print(result)

        elif choice == 3:
            break
        else:
            print("Invalid choice.")


