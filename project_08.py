import numpy as np


class DataAnalytics:
    """
    Encapsulates array creation, manipulation, math operations,
    searching/sorting/filtering, and statistical analysis using NumPy.
    """

    # ------------------------------------------------------------------
    # Constructor
    # ------------------------------------------------------------------
    def __init__(self, array=None):
        # "Private" attribute (name-mangled) -> encapsulation
        self.__array = np.array(array) if array is not None else None

    # ------------------------------------------------------------------
    # Encapsulation: controlled access to the private array
    # ------------------------------------------------------------------
    @property
    def array(self):
        return self.__array

    @array.setter
    def array(self, new_array):
        self.__array = np.array(new_array)

    def __check_array(self):
        """Private helper: raise an error if no array exists yet."""
        if self.__array is None:
            raise ValueError("No array has been created yet.")

    # ------------------------------------------------------------------
    # 1. Array Creation
    # ------------------------------------------------------------------
    @classmethod
    def create_from_input(cls, dimensions, shape, elements):
        """
        Class method: builds a DataAnalytics instance from raw input.
        dimensions: 1, 2, or 3
        shape: tuple describing the shape, e.g. (6,), (2,3), (2,2,2)
        elements: flat list of numbers
        """
        arr = np.array(elements).reshape(shape)
        return cls(arr)

    # ------------------------------------------------------------------
    # 2. Indexing & Slicing
    # ------------------------------------------------------------------
    def index_element(self, indices):
        self.__check_array()
        return self.__array[indices]

    def slice_array(self, row_slice, col_slice=None):
        self.__check_array()
        if col_slice is None:
            return self.__array[row_slice]
        return self.__array[row_slice, col_slice]

    # ------------------------------------------------------------------
    # 3. Combining & Splitting
    # ------------------------------------------------------------------
    def combine(self, other_array, axis=0):
        self.__check_array()
        return np.concatenate((self.__array, other_array), axis=axis)

    def split(self, num_splits, axis=0):
        self.__check_array()
        return np.array_split(self.__array, num_splits, axis=axis)

    # ------------------------------------------------------------------
    # 4. Mathematical Operations
    # ------------------------------------------------------------------
    def add(self, other):
        return self.__elementwise(other, "+")

    def subtract(self, other):
        return self.__elementwise(other, "-")

    def multiply(self, other):
        return self.__elementwise(other, "*")

    def divide(self, other):
        return self.__elementwise(other, "/")

    def __elementwise(self, other, op):
        """Private helper for element-wise math operations."""
        self.__check_array()
        other = np.array(other)
        if op == "+":
            return self.__array + other
        elif op == "-":
            return self.__array - other
        elif op == "*":
            return self.__array * other
        elif op == "/":
            return self.__array / other

    def dot_product(self, other):
        self.__check_array()
        return np.dot(self.__array, np.array(other))

    def matrix_multiply(self, other):
        self.__check_array()
        return np.matmul(self.__array, np.array(other))

    # ------------------------------------------------------------------
    # 5. Search, Sort, Filter
    # ------------------------------------------------------------------
    def search(self, value):
        self.__check_array()
        return np.argwhere(self.__array == value)

    def sort(self, ascending=True, axis=-1):
        self.__check_array()
        sorted_arr = np.sort(self.__array, axis=axis)
        if not ascending:
            sorted_arr = np.flip(sorted_arr, axis=axis)
        return sorted_arr

    def filter_values(self, condition_func):
        self.__check_array()
        return self.__array[condition_func(self.__array)]

    # ------------------------------------------------------------------
    # 6. Aggregating Functions
    # ------------------------------------------------------------------
    def sum(self):
        self.__check_array()
        return np.sum(self.__array)

    def mean(self):
        self.__check_array()
        return np.mean(self.__array)

    def median(self):
        self.__check_array()
        return np.median(self.__array)

    def std_dev(self):
        self.__check_array()
        return np.std(self.__array)

    def variance(self):
        self.__check_array()
        return np.var(self.__array)

    # ------------------------------------------------------------------
    # 7. Statistical Functions
    # ------------------------------------------------------------------
    def min_max(self):
        self.__check_array()
        return np.min(self.__array), np.max(self.__array)

    def percentile(self, q):
        self.__check_array()
        return np.percentile(self.__array, q)

    def correlation(self, other):
        self.__check_array()
        other = np.array(other)
        return np.corrcoef(self.__array.flatten(), other.flatten())[0, 1]

    # ------------------------------------------------------------------
    # Static utility method
    # ------------------------------------------------------------------
    @staticmethod
    def get_int_input(prompt):
        """Static method: safely read an integer from the user."""
        while True:
            try:
                return int(input(prompt))
            except ValueError:
                print("Invalid number, please try again.")

    def __str__(self):
        self.__check_array()
        return str(self.__array)


